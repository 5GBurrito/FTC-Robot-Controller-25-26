package org.firstinspires.ftc.teamcode.util

import com.qualcomm.robotcore.hardware.Gamepad

class GamepadState {
    var left_stick_x = 0f
    var left_stick_y = 0f
    var right_stick_x = 0f
    var right_stick_y = 0f

    var dpad_up = false
    var dpad_down = false
    var dpad_left = false
    var dpad_right = false

    var a = false
    var b = false
    var x = false
    var y = false

    var guide = false
    var start = false
    var back = false

    var left_bumper = false
    var right_bumper = false

    var left_stick_button = false
    var right_stick_button = false

    var left_trigger = 0f
    var right_trigger = 0f

    var circle = false
    var cross = false
    var triangle = false
    var square = false

    var share = false
    var options = false

    var touchpad = false
    var touchpad_finger_1 = false
    var touchpad_finger_2 = false
    var touchpad_finger_1_x = 0f
    var touchpad_finger_1_y = 0f
    var touchpad_finger_2_x = 0f
    var touchpad_finger_2_y = 0f

    var ps = false

    fun updateGamepadState(gamepad: Gamepad) {
        left_stick_x = gamepad.left_stick_x
        left_stick_y = gamepad.left_stick_y
        right_stick_x = gamepad.right_stick_x
        right_stick_y = gamepad.right_stick_y

        dpad_up = gamepad.dpad_up
        dpad_down = gamepad.dpad_down
        dpad_left = gamepad.dpad_left
        dpad_right = gamepad.dpad_right

        a = gamepad.a
        b = gamepad.b
        x = gamepad.x
        y = gamepad.y

        guide = gamepad.guide
        start = gamepad.start
        back = gamepad.back

        left_bumper = gamepad.left_bumper
        right_bumper = gamepad.right_bumper

        left_stick_button = gamepad.left_stick_button
        right_stick_button = gamepad.right_stick_button

        left_trigger = gamepad.left_trigger
        right_trigger = gamepad.right_trigger

        circle = gamepad.circle
        cross = gamepad.cross
        triangle = gamepad.triangle
        square = gamepad.square

        share = gamepad.share
        options = gamepad.options

        touchpad = gamepad.touchpad
        touchpad_finger_1 = gamepad.touchpad_finger_1
        touchpad_finger_2 = gamepad.touchpad_finger_2
        touchpad_finger_1_x = gamepad.touchpad_finger_1_x
        touchpad_finger_1_y = gamepad.touchpad_finger_1_y
        touchpad_finger_2_x = gamepad.touchpad_finger_2_x
        touchpad_finger_2_y = gamepad.touchpad_finger_2_y

        ps = gamepad.ps
    }
}

